import fs from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..', '..');
const deepCuratedReviewsPath = path.join(root, 'frontend', '.local-source-assets', 'atlas-ednea', 'deep-curated-reviews.json');
const highConfidenceBuilderPath = path.join(root, 'tools', 'knowledge', 'build-high-confidence-map-drafts.mjs');
const mapLocationsPath = path.join(root, 'frontend', 'src', 'knowledge', 'mapLocations.js');
const outputPath = path.join(root, 'frontend', 'src', 'knowledge', 'generated', 'medium-confidence-map-locations.js');
const reportJsonPath = path.join(root, 'docs', 'medium-confidence-map-drafts.json');
const reportMdPath = path.join(root, 'docs', 'medium-confidence-map-drafts.md');

const GENERATED_AT = new Date().toISOString();
const CALIBRATION_STATUS = 'draft_auto_medium_confidence';
const SOURCE = 'PDFs pt-BR Biblioteca Viva + KM-Agent/Atlas route draft';
const SOURCE_CONFIDENCE = 'confidence medium / pending_atlas_review';
const REVIEW_STATUS = 'pending_atlas_review';
const MERIDIAN_FROM_CODE = /^([A-Z]+)(\d+)$/;

function normalizePointCode(value) {
  return String(value || '')
    .trim()
    .toUpperCase()
    .replace(/\s+/g, '');
}

function codeParts(code) {
  const match = normalizePointCode(code).match(MERIDIAN_FROM_CODE);
  if (!match) return null;
  return {
    meridian: match[1],
    number: Number(match[2]),
  };
}

function roundPct(value) {
  return Number(value.toFixed(2));
}

function interpolate(nodes, number) {
  const sorted = [...nodes].sort((a, b) => a[0] - b[0]);
  if (number <= sorted[0][0]) return { xPct: sorted[0][1], yPct: sorted[0][2] };
  if (number >= sorted.at(-1)[0]) return { xPct: sorted.at(-1)[1], yPct: sorted.at(-1)[2] };

  for (let index = 0; index < sorted.length - 1; index += 1) {
    const start = sorted[index];
    const end = sorted[index + 1];
    if (number < start[0] || number > end[0]) continue;
    const ratio = (number - start[0]) / (end[0] - start[0]);
    return {
      xPct: roundPct(start[1] + ((end[1] - start[1]) * ratio)),
      yPct: roundPct(start[2] + ((end[2] - start[2]) * ratio)),
    };
  }

  return null;
}

function loadRouteModel(source) {
  const match = source.match(/const ROUTES = (\{[\s\S]*?\n\};)/);
  if (!match) throw new Error('ROUTES nao encontrado em build-high-confidence-map-drafts.mjs.');
  return Function(`"use strict"; return (${match[1].replace(/;$/, '')});`)();
}

function routeFor(routes, code) {
  const parts = codeParts(code);
  if (!parts) return null;
  const segments = routes[parts.meridian] || [];
  const segment = segments.find(item => parts.number >= item.from && parts.number <= item.to);
  if (!segment) return null;
  const point = interpolate(segment.nodes, parts.number);
  if (!point) return null;
  return {
    ...point,
    mapId: segment.mapId,
    view: segment.view,
    coordinateConfidence: segment.confidence,
    meridian: parts.meridian,
    number: parts.number,
  };
}

function existingCodesFromMapLocations(source) {
  return new Set(
    [...source.matchAll(/code:\s*['"]([^'"]+)['"]/g)]
      .map(match => normalizePointCode(match[1]))
      .filter(code => code && !code.startsWith('AURICULAR:')),
  );
}

function confidenceOf(review) {
  return review?.confidence
    || review?.enrichment?.confidence
    || review?.sourceReview?.confidence
    || null;
}

function isMediumPending(review) {
  return review?.status === REVIEW_STATUS
    && confidenceOf(review) === 'medium'
    && codeParts(review.code);
}

function generatedModule(locations) {
  const mapIds = [...new Set(locations.map(location => location.mapId))].sort();
  const views = [...new Set(locations.map(location => location.view))].sort();
  const coordinateConfidences = [...new Set(locations.map(location => location.coordinateConfidence))].sort();
  const compactRows = locations.map(location => [
    location.code,
    mapIds.indexOf(location.mapId),
    views.indexOf(location.view),
    location.xPct,
    location.yPct,
    coordinateConfidences.indexOf(location.coordinateConfidence),
  ]);

  return `// Generated by tools/knowledge/build-medium-confidence-map-drafts.mjs
// Draft visual coordinates only. Do not mark as clinically approved without visual/professional review.

const MAP_IDS = ${JSON.stringify(mapIds)};
const VIEWS = ${JSON.stringify(views)};
const COORDINATE_CONFIDENCES = ${JSON.stringify(coordinateConfidences)};
const GENERATED_AT = ${JSON.stringify(GENERATED_AT)};
const CALIBRATION_STATUS = ${JSON.stringify(CALIBRATION_STATUS)};
const SOURCE = ${JSON.stringify(SOURCE)};
const SOURCE_CONFIDENCE = ${JSON.stringify(SOURCE_CONFIDENCE)};
const REVIEW_STATUS = ${JSON.stringify(REVIEW_STATUS)};
const ROWS = ${JSON.stringify(compactRows)};

export const mediumConfidenceMapLocations = ROWS.map(([code, mapIdIndex, viewIndex, xPct, yPct, confidenceIndex]) => ({
  code,
  mapId: MAP_IDS[mapIdIndex],
  view: VIEWS[viewIndex],
  xPct,
  yPct,
  approved: false,
  calibrationStatus: CALIBRATION_STATUS,
  coordinateConfidence: COORDINATE_CONFIDENCES[confidenceIndex],
  sourceConfidence: SOURCE_CONFIDENCE,
  source: SOURCE,
  reviewStatus: REVIEW_STATUS,
  approvalMode: 'none',
  localOnly: true,
  requiresProfessionalAudit: true,
  generatedAt: GENERATED_AT,
}));
`;
}

function reportMarkdown(report) {
  const byMap = Object.entries(report.byMap)
    .map(([, value]) => `- ${value.label}: ${value.count} rascunhos`)
    .join('\n');
  const blocked = report.blocked.length
    ? report.blocked.map(item => `- ${item.code}: ${item.reason}`).join('\n')
    : '- Nenhum.';

  return `# Coordenadas visuais automáticas (confiança média)

Gerado em: ${report.generatedAt}

## Resultado

- Pontos pending_atlas_review com confiança média: ${report.counts.mediumPendingReviews}
- Entradas com requiresProfessionalAudit true na origem local: ${report.counts.requiresProfessionalAuditTrue}
- Já tinham alguma coordenada visual canônica: ${report.counts.alreadyMappedInPointLocations}
- Rascunhos de coordenada gerados: ${report.counts.generated}
- Sem rota visual disponível: ${report.counts.blocked}

## Por mapa de rota

${byMap || '- Nenhum.'}

## Status

Todas as coordenadas geradas ficam como \`${CALIBRATION_STATUS}\`, \`approved: false\`, \`requiresProfessionalAudit: true\` e \`sourceConfidence: ${SOURCE_CONFIDENCE}\`.
A confiança média pertence à ficha/fonte do ponto; a posição visual ainda precisa de revisão anatômica e profissional.

## Bloqueados

${blocked}
`;
}

async function main() {
  const [reviewsPayload, routeBuilderSource, mapLocationsSource] = await Promise.all([
    fs.readFile(deepCuratedReviewsPath, 'utf8').then(JSON.parse),
    fs.readFile(highConfidenceBuilderPath, 'utf8'),
    fs.readFile(mapLocationsPath, 'utf8'),
  ]);

  const routes = loadRouteModel(routeBuilderSource);
  const alreadyMappedCodes = existingCodesFromMapLocations(mapLocationsSource);
  const reviews = Array.isArray(reviewsPayload.reviews) ? reviewsPayload.reviews : [];
  const mediumPending = reviews
    .filter(isMediumPending)
    .sort((a, b) => normalizePointCode(a.code).localeCompare(normalizePointCode(b.code), 'en', { numeric: true }));

  const generated = [];
  const blocked = [];

  for (const review of mediumPending) {
    const code = normalizePointCode(review.code);
    const route = routeFor(routes, code);
    if (!route) {
      blocked.push({ code, reason: 'Sem rota anatomica configurada para o mapa atual.' });
      continue;
    }
    generated.push({
      code,
      ...route,
    });
  }

  const byMap = generated.reduce((acc, location) => {
    if (!acc[location.mapId]) {
      acc[location.mapId] = {
        label: location.mapId,
        count: 0,
      };
    }
    acc[location.mapId].count += 1;
    return acc;
  }, {});

  const alreadyMappedInPointLocations = generated.filter(location => alreadyMappedCodes.has(location.code)).length;
  const report = {
    schemaVersion: 1,
    generatedAt: GENERATED_AT,
    sourceConfidence: SOURCE_CONFIDENCE,
    reviewStatus: REVIEW_STATUS,
    calibrationStatus: CALIBRATION_STATUS,
    counts: {
      mediumPendingReviews: mediumPending.length,
      requiresProfessionalAuditTrue: mediumPending.filter(review => review.requiresProfessionalAudit === true).length,
      alreadyMappedInPointLocations,
      generated: generated.length,
      blocked: blocked.length,
    },
    byMap,
    blocked,
    generated,
  };

  await fs.mkdir(path.dirname(outputPath), { recursive: true });
  await fs.writeFile(outputPath, generatedModule(generated), 'utf8');
  await fs.writeFile(reportJsonPath, `${JSON.stringify(report, null, 2)}\n`, 'utf8');
  await fs.writeFile(reportMdPath, reportMarkdown(report), 'utf8');

  console.log(JSON.stringify({
    output: path.relative(root, outputPath),
    reportJson: path.relative(root, reportJsonPath),
    reportMarkdown: path.relative(root, reportMdPath),
    counts: report.counts,
    byMap,
  }, null, 2));
}

main().catch(error => {
  console.error(error);
  process.exitCode = 1;
});
